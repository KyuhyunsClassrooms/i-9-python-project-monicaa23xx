# [1] 데이터: 환자 현황 (2차원 리스트)
# 한 행: 환자 1명 / 열: [0]번호, [1]수술명, [2]통증점수, [3]부작용
patients = [
    ["P001", "복강경수술", 6, "오심"],
    ["P002", "충수절제술", 2, "없음"],
    ["P003", "제왕절개술", 3, "어지러움"]
]

# [2] 기능: 환자의 상태를 진단하고 지시사항을 정하는 함수
def analyze_patient(patient):
    # 복합 조건 판정 (부작용이 있으면 주입 중단, 없는데 통증이 4 이상이면 추가 주입)
    if patient[3] != "없음":
        return f"🚨 [주입 중단] 부작용({patient[3]}) 발생! PCA 잠금 및 보고"
    elif patient[2] >= 4:
        return f"⚠️ [추가 주입] 통증({patient[2]}점) 높음! 추가 주입 고려"
    else:
        return "✅ [유지] 통증 및 부작용 안정적임"

# [3] 프로그램 실시간 작동 (메인 흐름)
print("=== 🏥 환자 통증 및 부작용 모니터링 시스템 ===")

# 1. 반복문으로 2차원 리스트를 돌며 환자 실시간 분석 (필수 조건)
print("\n[현재 병동 환자 실시간 상태 분석]")
for p in patients:
    print(f"▶ 환자 {p[0]} ({p[1]}) -> {analyze_patient(p)}")
    
print("\n" + "="*40)

# 2. 키보드로 새로운 환자 정보 수집 및 등록
print("[신규 환자 정보 등록]")
num = input("환자 번호: ")
op = input("수술명: ")
pain = int(input("통증 점수(0~10): ")) # 숫자로 형변환
side = input("부작용 증상(없음/오심/어지러움 등): ")

# 3. 입력된 데이터를 리스트로 묶어 기존 2차원 리스트에 추가
new_patient = [num, op, pain, side]
patients.append(new_patient)

print(f"\n[등록 완료] {num}번 환자가 병동 데이터에 추가되었습니다.")
print(f"새 환자 실시간 분석: {analyze_patient(new_patient)}")